#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*ptr;
	size_t	start;
	size_t	end;
	size_t	len;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	len = end - start;
	if (len == 0)
		ptr = malloc(1);
	else
		ptr = malloc(len + 1);
	if (!ptr)
		return (NULL);
	ft_memcpy(ptr, &s1[start], len);
	ptr[len] = '\0';
	return (ptr);
}
